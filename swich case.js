//Q. print name with the help of swich case
let name ="kavita"

switch(name){
   case"soniya":
   console.log("hello soniya");
   break;
   case"megha":
   console.log("hello megha");
   break;
    case"kavita":
   console.log("hello kavita");
   break;
   case"hema":
   console.log("hello hema");
   break;
   default:
       console.log("invalid or error");
}


